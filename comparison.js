let intValue = 80; 
let strValue = "80";


//result : 
console.log("intValue == strValue is ", intValue == strValue) //both are equal to 80 (== means comparing the value not the type) the result is true 
console.log("intValue === strValue is ", intValue === strValue)  //=== means comparing the value AND the type (the result is false) 
console.log("intValue != strValue is ", intValue != strValue)  //false
console.log("intValue !== strValue is ", intValue !== strValue) //true 

console.log("intValue > strValue is ", intValue > strValue) //false
console.log("intValue < strValue is ", intValue < strValue) //false
console.log("intValue >= strValue is ", intValue >= strValue)  //true
console.log("intValue <= strValue is ", intValue <= strValue) //true 